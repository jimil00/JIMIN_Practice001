package practice001;

public class StringOperation {

	public static void main(String[] args) {
		
		System.out.println("Hello World".length()); //글자수
		System.out.println("Hello, leezche ... bye.".replace("leezche", "egoing")); //글자 대체
		System.out.println("Hello, [[[name]]] ... bye.".replace("[[[name]]]","duru"));

	}

}
